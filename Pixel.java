
public class Pixel {

	
	
	
	private short red;
	private short green;
	private short blue;
	private short alpha;
	
	public Pixel(short r,short g,short b,short a) {
		red=r;
		green=g;
		blue=b;
		alpha=a;
	}
	
	public short getRed() {
		return red;
	}
	public void setRed(short red) {
		this.red = red;
	}
	public short getGreen() {
		return green;
	}
	public void setGreen(short green) {
		this.green = green;
	}
	public short getBlue() {
		return blue;
	}
	public void setBlue(short blue) {
		this.blue = blue;
	}
	public short getAlpha() {
		return alpha;
	}
	public void setAlpha(short alpha) {
		this.alpha = alpha;
	}
	
	
	
	
	
}
